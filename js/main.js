let c_code = "EUR";

switch (c_code) {
    case "EUR": console.log("Euro");
        break;
    case "INR": console.log("Indian Rupee");
        break;
    case "AUD": console.log("Australian Dollar");
        break;
    case "IDR": console.log("Rupiah");
        break;
    case "NPR": console.log("Nepalese Rupee");
        break;
    case "USD": console.log("US Dollar");
        break;
    case "PKR": console.log("Pakistan Rupee");
        break;
    default :console.log("Sorry,  Currency code " + c_code + " is not exist.");
}



